#include<iostream>
using namespace std;

int main()
{
    int a;
    a = 3;

    cout<<"size of int "<<sizeof(a)<<endl;

    float b;
    cout<<"size of float "<<sizeof(b)<<endl;

    char c;
    cout<<"size of char "<<sizeof(c)<<endl;

    bool d;
    cout<<"size of boolean "<<sizeof(d)<<endl;

    short int x;
    long int y;

    cout<<"size of short_int "<<sizeof(x)<<endl;
    cout<<"size of long_int "<<sizeof(y)<<endl;
}